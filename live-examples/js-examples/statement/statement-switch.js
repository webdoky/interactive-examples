const expr = 'Папайя';
switch (expr) {
  case 'Помаранчі':
    console.log('Помаранчі — по $0.59 за кіло.');
    break;
  case 'Манго':
  case 'Папайя':
    console.log('Манго та папайя — по $2.79 за кіло.');
    // Очікуваний вивід: "Манго та папайя — по $2.79 за кіло."
    break;
  default:
    console.log(`Вибачте, у нас закінчились ${expr}.`);
}
