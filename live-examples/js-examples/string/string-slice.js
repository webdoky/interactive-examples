const str = 'Гей, хлопці, не вспію - на ґанку ваша файна їжа знищується бурундучком.';

console.log(str.slice(48));
// Очікуваний вивід: "знищується бурундучком."

console.log(str.slice(5, 21));
// Очікуваний вивід: "хлопці, не вспію"

console.log(str.slice(-12));
// Очікуваний вивід: "бурундучком."

console.log(str.slice(-23, -13));
// Очікуваний вивід: "знищується"
