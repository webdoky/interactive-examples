const sentence = 'Гей, хлопці, не вспію - на ґанку ваша файна їжа знищується бурундучком.';

let index = 5;

console.log(`За індексом ${index} було повернено символ ${sentence.at(index)}`);
// очікуваний вивід: "За індексом 5 було повернено символ х"

index = -4;

console.log(`За індексом ${index} було повернено символ ${sentence.at(index)}`);
// очікуваний вивід: "За індексом -4 було повернено символ к"
